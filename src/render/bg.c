#include "ft_bonus.h"

void	draw_bg(t_data *data)
{
	int	i;
	int	x;

	i = 0;
	while (i < 4)
	{
		x = (-data->scn.camera / (8 - (i * 2))) / 2;
		draw_img(&data->frame, &data->bg[i], x, 0);
		i++;
	}
}
